    private void editar(String message){
        int answer = JOptionPane.showConfirmDialog(
            this,
            message,
            "An Inane Question",
            JOptionPane.YES_NO_OPTION);

        if(answer==JOptionPane.YES_OPTION){
            try {
                controller.nou(textFieldToText());
                this.jButton9.setEnabled(false);
            this.jButton8.setEnabled(false);
            //controller.save();
            habilitaBotons();
            refreshRegistre();
            
            } catch (IOException ex) {
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            JOptionPane.showMessageDialog(this, "S'han realitzat els canvis");
            
        }
        else {
            
            JOptionPane.showMessageDialog(this, "No s'han realitzat els canvis");
        }
    }

