    private void refreshRegistre() {
        int cursor = controller.getCursor()+1;
        if (cursor < 999) {
            this.jLabel6.setText("registre: " + cursor + " de: " + controller.getRegistres());
        } else {
            this.jLabel6.setText("registre: ..." + " de: " + controller.getRegistres());
        }

    }
