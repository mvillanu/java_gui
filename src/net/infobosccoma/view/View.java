    private boolean textIsValid(){
       boolean edat = !jTextField5.getText().matches("\\D");
       boolean altres = !jTextField1.getText().matches("\\w")&&
               !jTextField2.getText().matches("\\w")&&
               !jTextField3.getText().matches("\\w")&&
               !jTextField4.getText().matches("\\w");
       if(!edat ||!altres){
           return false;
       }else{
           return true;
       }
    }
    private void mouseClickDesa(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mouseClickDesa

        if(textIsValid()){
            if (controller.exists(jTextField1.getText())) {
            editar("Aquesta persona ja existeix, la vols editar?");
            
        } else {
            this.jButton9.setEnabled(false);
            this.jButton8.setEnabled(false);

            try {
                
                controller.nou(textFieldToText());
            } catch (IOException ex) {
                showMessage(ex.getMessage());
            }
            //controller.save();
            habilitaBotons();
            
            mouseClickPrimer(null);
        } 
        }else{
            showMessage("Dades no vàlides");
        }
       


    }//GEN-LAST:event_mouseClickDesa

